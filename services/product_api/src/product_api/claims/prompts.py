import json
from typing import Any

from shared.schemas import ChatMessage

_BLOCK_LIMIT_NOTES = {
    "legal_basis": "не добавлять правовой блок со ссылками на нормы права",
    "penalty_section": "не добавлять расчет санкций или неустойки",
    "attachments": "не добавлять раздел приложений",
}

_RISK_LIMIT_NOTES = {
    "case_type_uncertain": "если тип отношений неочевиден, использовать осторожные нейтральные формулировки",
    "contract_status_uncertain": "не утверждать факт подписания договора без данных",
    "no_supporting_documents": "не перечислять документы как приложенные, если это не подтверждено данными",
    "high_claim_amount": "не делать категоричных выводов без дополнительной проверки",
}


def build_preview_generation_messages(
    *,
    input_text: str,
    case_type: str | None,
    normalized_data: dict[str, Any] | None,
    allowed_blocks: list[str],
    blocked_blocks: list[str],
    risk_flags: list[str],
    derived_preview_data: dict[str, Any] | None = None,
) -> list[ChatMessage]:
    system_prompt = (
        "Ты юрист по претензионной работе в РФ. "
        "Сформируй только plain text тела preview досудебной претензии на русском языке: 1–2 содержательных абзаца. "
        "Абзац 1 описывает основание отношений: стороны, их роли, договор или представленные документы; номер и дату договора можно использовать только если они есть в данных. "
        "Абзац 1 должен быть юридическим opening paragraph: сначала укажи договор или иное основание отношений и стороны; затем укажи, что на этом основании между сторонами возникли взаимные обязательства. "
        "Для case_type=supply используй роли поставщик/покупатель и обязанности передать товар / принять и оплатить товар; для case_type=services — исполнитель/заказчик и обязанности оказать услуги / принять и оплатить услуги; для case_type=contract_work — подрядчик/заказчик и обязанности выполнить работы / принять и оплатить работы. "
        "Если case_type отсутствует, используй нейтральные формулировки без специальных ролей. "
        "Номер договора и дату договора бери только из normalized_data: не выдумывай номер, не выдумывай дату, не выдумывай стороны, договор или предмет. "
        "Если contract_signed=false или данные указывают, что договор не подписан или не заключён, не утверждай \"был заключён договор\"; пиши осторожно: отношения сторон связаны с договором/документами или обязательственные отношения возникли на основании представленных документов. "
        "Если договора нет, но есть documents_mentioned, пиши про представленные документы, не выдумывай номера/даты документов, не копируй technical ids, переводи типы документов в обычные русские названия. "
        "Фраза \"обязалось произвести оплату\" допустима только как описание договорной обязанности; не используй \"требуем оплатить\", \"просим оплатить\" или \"погасить задолженность в срок\" как финальное требование. "
        "Абзац 2 описывает исполнение кредитором и нарушение обязанности оплаты должником как логическую цепочку: "
        "исполнение обязательств кредитором; передача товара, оказание услуг или выполнение работ; возникновение встречной обязанности должника оплатить; "
        "неисполнение оплаты; образование задолженности; срок оплаты, если он есть в normalized_data.payment_due_date; "
        "период просрочки, если он есть в derived_preview_data.overdue_days; состояние частичных оплат. "
        "Для абзаца 2 при case_type=supply используй поставщик/покупатель, передача товара, оплата поставленного товара или оплата полученного товара; "
        "при case_type=services — исполнитель/заказчик, оказание услуг, оплата оказанных услуг; "
        "при case_type=contract_work — подрядчик/заказчик, выполнение работ, оплата выполненных работ. "
        "Если case_type отсутствует, в абзаце 2 используй нейтральные формулировки: сторона, заявляющая требование; обязанная сторона; денежное обязательство. "
        "Срок оплаты бери только из normalized_data.payment_due_date. Количество дней просрочки бери только из derived_preview_data.overdue_days; "
        "если overdue_days отсутствует, не пиши конкретное число дней и не рассчитывай просрочку самостоятельно. "
        "Если partial_payments_present=false, можно указать отсутствие сведений о частичном погашении и сохранение задолженности в полном размере. "
        "Если partial_payments_present=null или поле отсутствует, не утверждай, что частичных оплат не было. "
        "Если partial_payments_present=true, не пересчитывай остаток самостоятельно и не выдумывай суммы/даты частичных оплат. "
        "Сумму задолженности бери только из normalized_data.debt_amount; не выдумывай сумму, не пересчитывай остаток задолженности и не добавляй сумму прописью, если она отсутствует в данных. "
        "Не возвращай markdown. Не возвращай JSON. "
        "Не используй списки, bullets или нумерацию. "
        "Не дублируй шапку документа. "
        "Не генерируй заголовок ПРЕТЕНЗИЯ, исходящий номер, дату самой претензии, контакты, приложения или подпись. "
        "Не добавляй финальное требование оплатить, судебный блок, исковое заявление или полный правовой блок со статьями ГК РФ/АПК РФ. "
        "Не добавляй demo/paywall текст: Демо-версия, полная версия после оплаты, оплатите доступ. "
        "Не используй английские технические названия блоков. "
        "Не пиши строки header, facts, legal_basis, demand_block, allowed_blocks, blocked_blocks, risk_flags. "
        "Не добавляй строки \"Кому:\", \"От кого:\", \"Адрес:\", \"E-mail:\", \"E-mail/контакты:\", \"Email:\", \"Контакты:\", \"Телефон:\". "
        "Не выдумывай факты, которых нет в исходных данных. "
        "Если данных недостаточно, используй осторожные нейтральные формулировки без служебных пометок."
    )
    context = {
        "case_type": case_type,
        "normalized_data": normalized_data or {},
        "generation_limits": _build_generation_limit_notes(
            blocked_blocks=blocked_blocks,
            risk_flags=risk_flags,
        ),
    }
    if derived_preview_data:
        context["derived_preview_data"] = derived_preview_data
    user_prompt = (
        "Ситуация пользователя:\n"
        f"{input_text.strip()}\n\n"
        "Данные для учета. Не копируй формат и названия полей в ответ:\n"
        f"{json.dumps(context, ensure_ascii=False)}\n\n"
        "Верни только 1–2 абзаца тела preview. "
        "Не добавляй labels, списки технических ограничений, финальное требование, судебный блок, статьи закона, приложения, подпись, demo/paywall текст или сведения, которых нет в данных."
    )
    return [
        ChatMessage(role="system", content=system_prompt),
        ChatMessage(role="user", content=user_prompt),
    ]


def _build_generation_limit_notes(
    *,
    blocked_blocks: list[str],
    risk_flags: list[str],
) -> list[str]:
    notes: list[str] = []
    for block_name in blocked_blocks:
        note = _BLOCK_LIMIT_NOTES.get(block_name)
        if note and note not in notes:
            notes.append(note)

    for risk_flag in risk_flags:
        note = _RISK_LIMIT_NOTES.get(risk_flag)
        if note and note not in notes:
            notes.append(note)

    if not notes:
        notes.append("не добавлять сведения, которых нет во входных данных")
    return notes
