type ClaimDocumentDemoCaseType = keyof typeof CLAIM_DOCUMENT_DEMO_TEXTS

const SUPPORTED_CASE_TYPES = new Set<ClaimDocumentDemoCaseType>([
  'supply',
  'contract_work',
  'services',
])

export function getClaimDocumentDemoText(
  caseType: string | null | undefined,
  claimId: string | number | null | undefined,
): string {
  const normalizedCaseType = normalizeCaseType(caseType)
  const variants = CLAIM_DOCUMENT_DEMO_TEXTS[normalizedCaseType]
  const seedClaimId = normalizeClaimId(claimId)

  if (!seedClaimId) {
    return variants[0] ?? CLAIM_DOCUMENT_DEMO_TEXTS.default[0]
  }

  const index = stableTextIndex(`${normalizedCaseType}:${seedClaimId}`, variants.length)
  return variants[index] ?? CLAIM_DOCUMENT_DEMO_TEXTS.default[0]
}

function normalizeCaseType(caseType: string | null | undefined): ClaimDocumentDemoCaseType {
  const normalized = caseType?.trim().toLowerCase()
  if (normalized && SUPPORTED_CASE_TYPES.has(normalized as ClaimDocumentDemoCaseType)) {
    return normalized as ClaimDocumentDemoCaseType
  }
  return 'default'
}

function normalizeClaimId(claimId: string | number | null | undefined): string | null {
  if (claimId === null || claimId === undefined) {
    return null
  }

  const normalized = String(claimId).trim()
  return normalized || null
}

function stableTextIndex(seed: string, variantsLength: number): number {
  if (variantsLength <= 1) {
    return 0
  }

  let hash = 2166136261
  for (let index = 0; index < seed.length; index += 1) {
    hash ^= seed.charCodeAt(index)
    hash = Math.imul(hash, 16777619)
  }

  return (hash >>> 0) % variantsLength
}
